class TestCreateOrUpdateReportFromExistingData(TestReportService):
    """Tests for create_or_update_report_from_existing_data()"""

    def test_creates_report_with_all_existing_data(
        self,
        test_db: Session,
        setup_project,
        setup_tasks,
        setup_annotations,
        setup_generations,
        setup_evaluations,
        test_user,
    ):
        """Test that report is created with all existing project data"""
        from report_service import create_or_update_report_from_existing_data

        report = create_or_update_report_from_existing_data(test_db, setup_project.id, test_user.id)

        assert report is not None

        # All sections should be completed
        assert report.content["sections"]["project_info"]["status"] == "completed"
        assert report.content["sections"]["data"]["status"] == "completed"
        assert report.content["sections"]["annotations"]["status"] == "completed"
        assert report.content["sections"]["generation"]["status"] == "completed"
        assert report.content["sections"]["evaluation"]["status"] == "completed"

        # Check data
        assert report.content["sections"]["data"]["task_count"] == 5
        assert report.content["sections"]["annotations"]["annotation_count"] == 3
        assert len(report.content["sections"]["generation"]["models"]) == 2

    def test_updates_existing_report(self, test_db: Session, setup_project, setup_tasks, test_user):
        """Test that existing report is updated rather than creating a new one"""
        from report_service import (
            create_initial_report_draft,
            create_or_update_report_from_existing_data,
        )

        # Create initial report
        initial_report = create_initial_report_draft(test_db, setup_project.id, test_user.id)
        initial_id = initial_report.id

        # Update with existing data
        updated_report = create_or_update_report_from_existing_data(
            test_db, setup_project.id, test_user.id
        )

        # Should be the same report
        assert updated_report.id == initial_id
        # But with updated content
        assert updated_report.content["sections"]["data"]["status"] == "completed"
