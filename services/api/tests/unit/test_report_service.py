class TestUpdateReportDataSection(TestReportService):
    """Tests for update_report_data_section()"""

    def test_updates_data_section_with_task_count(
        self, test_db: Session, setup_project, setup_tasks, test_user
    ):
        """Test that data section is updated with correct task count"""
        from report_service import create_initial_report_draft, update_report_data_section

        # Create initial report
        create_initial_report_draft(test_db, setup_project.id, test_user.id)

        # Update data section
        report = update_report_data_section(test_db, setup_project.id)

        assert report is not None
        assert report.content["sections"]["data"]["status"] == "completed"
        assert report.content["sections"]["data"]["task_count"] == 5
        assert "data" in report.content["metadata"]["sections_completed"]

    def test_preserves_custom_text(self, test_db: Session, setup_project, setup_tasks, test_user):
        """Test that custom text is preserved during update"""
        from sqlalchemy.orm.attributes import flag_modified

        from report_service import create_initial_report_draft, update_report_data_section

        # Create initial report
        report = create_initial_report_draft(test_db, setup_project.id, test_user.id)

        # Add custom text - must flag as modified for JSONB column
        report.content["sections"]["data"]["custom_text"] = "Custom data description"
        flag_modified(report, "content")
        test_db.commit()

        # Update data section
        updated_report = update_report_data_section(test_db, setup_project.id)

        assert (
            updated_report.content["sections"]["data"]["custom_text"] == "Custom data description"
        )

    def test_returns_none_for_nonexistent_report(self, test_db: Session, setup_project):
        """Test that update returns None if report doesn't exist"""
        from report_service import update_report_data_section

        result = update_report_data_section(test_db, setup_project.id)
        assert result is None


