class TestGetEvaluationChartsData(TestReportService):
    """Tests for get_evaluation_charts_data()"""

    def test_returns_charts_by_model_and_method(
        self, test_db: Session, setup_project, setup_evaluations, test_evaluation_types
    ):
        """Test that chart data is organized by model and method"""
        from report_service import get_evaluation_charts_data

        chart_data = get_evaluation_charts_data(test_db, setup_project.id)

        # Check by_model structure
        assert "by_model" in chart_data
        assert "gpt-4" in chart_data["by_model"]
        assert "claude-3-opus" in chart_data["by_model"]
        assert chart_data["by_model"]["gpt-4"]["exact_match"] == 0.85

        # Check by_method structure
        assert "by_method" in chart_data
        assert "exact_match" in chart_data["by_method"]
        assert chart_data["by_method"]["exact_match"]["gpt-4"] == 0.85

    def test_includes_metric_metadata(
        self, test_db: Session, setup_project, setup_evaluations, test_evaluation_types
    ):
        """Test that chart data includes metric metadata"""
        from report_service import get_evaluation_charts_data

        chart_data = get_evaluation_charts_data(test_db, setup_project.id)

        assert "metric_metadata" in chart_data
        metadata = chart_data["metric_metadata"]

        # Check that metadata is populated for metrics found
        for metric_name in ["exact_match", "f1", "bleu"]:
            assert metric_name in metadata
            assert "higher_is_better" in metadata[metric_name]
            assert "range" in metadata[metric_name]
            assert "name" in metadata[metric_name]

    def test_llm_judge_metrics_get_correct_scale(self, test_db: Session, setup_project, test_user):
        """Test that LLM Judge metrics get 1-5 scale in metadata"""
        from models import EvaluationRun
        from report_service import get_evaluation_charts_data

        # Create evaluation with LLM Judge metrics
        llm_eval = EvaluationRun(
            id=str(uuid.uuid4()),
            project_id=setup_project.id,
            model_id="gpt-4",
            evaluation_type_ids=["llm_judge_helpfulness", "llm_judge_correctness"],
            metrics={
                "llm_judge_helpfulness": 4.2,
                "llm_judge_correctness": 3.8,
                "llm_judge_pairwise": 0.65,
            },
            status="completed",
            created_by=test_user.id,
        )
        test_db.add(llm_eval)
        test_db.commit()

        chart_data = get_evaluation_charts_data(test_db, setup_project.id)

        metadata = chart_data["metric_metadata"]

        # LLM Judge metrics should have 1-5 scale
        assert metadata["llm_judge_helpfulness"]["range"] == [1, 5]
        assert metadata["llm_judge_correctness"]["range"] == [1, 5]

        # Pairwise should have 0-1 scale
        assert metadata["llm_judge_pairwise"]["range"] == [0, 1]

    def test_returns_empty_for_no_evaluations(self, test_db: Session, setup_project):
        """Test that empty data is returned when no evaluations exist"""
