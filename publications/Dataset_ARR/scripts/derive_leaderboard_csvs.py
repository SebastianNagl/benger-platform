def _to_float(v):
    if v is None or isinstance(v, bool):
        return None
    if isinstance(v, (int, float)):
        return float(v)
    try:
        return float(v)
    except (TypeError, ValueError):
        return None


def bootstrap_ci(values, b=BOOTSTRAP_B, seed=BOOTSTRAP_SEED):
    """Bootstrap percentile 95% CI on the mean. Returns (mean, lo, hi, n)."""
    xs = np.asarray([v for v in values if v is not None], dtype=float)
    n = int(xs.size)
    if n == 0:
        return (None, None, None, 0)
    mean = float(xs.mean())
    if n < 2:
        return (mean, mean, mean, n)
    res = sp_stats.bootstrap(
        (xs,), np.mean, n_resamples=b, method="percentile",
        random_state=np.random.default_rng(seed),
    )
    return (mean, float(res.confidence_interval.low),
            float(res.confidence_interval.high), n)


# Per-metric formatting: width controls how many decimals appear in
# `mean [lo, hi]`. Judge raw and grade points read better at 1 decimal;
# pass / accuracy as percentages at 1 decimal; everything else at 3 decimals
# (fine-grained metrics like BLEU / ROUGE / BERTScore where the third decimal
# carries signal).
_METRIC_DECIMALS = {
    "judge_raw": 1,
    "grade_points": 2,
    "pass_rate": ("pct", 1),
    "judge_pass_rate": ("pct", 1),
    "accuracy": ("pct", 1),
    "human_raw": 1,
    "human_grade_points": 2,
    "human_pass_rate": ("pct", 1),
}
_DEFAULT_DECIMALS = 3


def _format_metric_cell(metric, mean, lo, hi):
    if mean is None:
        return ""
    spec = _METRIC_DECIMALS.get(metric, _DEFAULT_DECIMALS)
    if isinstance(spec, tuple) and spec[0] == "pct":
        d = spec[1]
        return f"{mean*100:.{d}f}% [{lo*100:.{d}f}, {hi*100:.{d}f}]"
    d = spec
    return f"{mean:.{d}f} [{lo:.{d}f}, {hi:.{d}f}]"


def write_wide_csv(path: Path, rows, metrics, system_label="system",
                   count_columns=("n_generations",)):
    """Write a wide CSV: system, <count cols>, then one column per metric
    holding `mean [ci_low, ci_high]` (95% bootstrap CI).

    rows is a list of (label, counts: dict, per_metric: dict) where counts
    maps each name in `count_columns` to an int (missing keys render as "").
    """
    header = [system_label] + list(count_columns) + [f"{m} [95% CI]" for m in metrics]

    out_rows = []
    for label, counts, per_metric in rows:
        row = [label] + [counts.get(c, "") for c in count_columns]
        for m in metrics:
            mean, lo, hi, _n = bootstrap_ci(per_metric.get(m, []))
            row.append(_format_metric_cell(m, mean, lo, hi))
        out_rows.append(row)

    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(header)
        for r in out_rows:
            w.writerow(r)
    return out_rows


def _load_human_grade_buckets():
    """Bucket the human-grader scores by the row label used in build_benchathon.

    Returns (buckets, counts):
      buckets: bucket_key -> {"human_raw"|"human_grade_points"|"human_pass_rate": [floats]}
      counts:  bucket_key -> int (total grader×solution rows)

    Bucket keys:
      - "Human (traditional)"   for solution_type == human_traditional
      - "Human (co-creation)"   for solution_type == human_co_creation
      - model_id (raw, e.g. "claude-sonnet-4-6")   for solution_type == llm_system

    Reads benchathon_human_grades.json — whatever's in that file (mock today,
    real once expert grading lands and derive_paper_exports.py is rerun).
    """
    if not HUMAN_GRADES.exists():
        return {}, {}
    grades = json.loads(HUMAN_GRADES.read_text(encoding="utf-8"))
    buckets: dict[str, dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    counts: dict[str, int] = defaultdict(int)
    for g in grades:
        stype = g.get("solution_type")
        sou = g.get("system_or_user")
        if stype == "human_traditional":
            key = "Human (traditional)"
        elif stype == "human_co_creation":
            key = "Human (co-creation)"
        elif stype == "llm_system" and sou:
            key = sou
        else:
            continue
        raw = _to_float(g.get("raw_score"))
        gp = _to_float(g.get("grade_points"))
        passed = g.get("passed")
        if raw is not None:
            buckets[key]["human_raw"].append(raw)
        if gp is not None:
            buckets[key]["human_grade_points"].append(gp)
        if passed is not None:
            buckets[key]["human_pass_rate"].append(1.0 if passed else 0.0)
        counts[key] += 1
    return buckets, counts


