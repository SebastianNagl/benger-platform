def build_grundprinzipien():
    if not GP_SRC.exists():
        sys.exit(f"missing {GP_SRC}")
    print(f"[gp] loading {GP_SRC.name} ...", file=sys.stderr)
    with GP_SRC.open(encoding="utf-8") as f:
        export = json.load(f)

    sys_vals: dict[str, dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    sys_gens: dict[str, int] = defaultdict(int)

    for task in export["tasks"]:
        for gen in task.get("generations") or []:
            mid = gen.get("model_id")
            if not mid:
                continue
            sys_gens[mid] += 1

            gen_judge = {"judge_raw": None, "judge_pass_rate": None,
                         # Ja/Nein accuracy: normalised decision match, not the
                         # buggy exact-match metric. See scripts/_gp_decision.py.
                         "accuracy": decision_accuracy(
                             gen.get("response_content"),
                             (task.get("data") or {}).get("binary_solution"))}
            gen_metric_vals: dict[str, float] = {}

            for ev in gen.get("evaluations") or []:
                m = ev.get("metrics") or {}

                if ("raw_score" in m and "llm_judge_custom" in m
                        and gen_judge["judge_raw"] is None):
                    raw = _to_float(m.get("raw_score"))
                    if raw is not None:
                        gen_judge["judge_raw"] = raw * 100.0
                    passed = ev.get("passed")
                    if passed is not None:
                        gen_judge["judge_pass_rate"] = 1.0 if passed else 0.0
                    continue

                if "accuracy" in m:   # recomputed above; ignore the stored metric
                    continue

                for k in ("bleu", "rouge", "meteor", "chrf",
                          "bertscore", "moverscore", "semantic_similarity"):
                    if k in m and isinstance(m[k], dict) and k not in gen_metric_vals:
                        v = _to_float(m[k].get("value"))
                        if v is not None:
                            gen_metric_vals[k] = v

            for k, v in gen_judge.items():
                if v is not None:
                    sys_vals[mid][k].append(v)
            for k, v in gen_metric_vals.items():
                sys_vals[mid][k].append(v)

    sys_order = sorted(
        sys_vals.keys(),
        key=lambda s: -(statistics.fmean(sys_vals[s].get("judge_raw") or [0.0])),
    )
    rows = [(s, {"n_generations": sys_gens[s]}, dict(sys_vals[s])) for s in sys_order]
    out = write_wide_csv(OUT_GP, rows, GP_METRICS, system_label="system")
    print(f"[gp] wrote {OUT_GP.relative_to(HERE)} ({len(out)} rows)")
    return rows


