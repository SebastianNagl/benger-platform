def build_zjs():
    if not ZJS_SRC.exists():
        sys.exit(f"missing {ZJS_SRC}")
    print(f"[zjs] streaming {ZJS_SRC} (3.6 GB; takes ~few minutes) ...", file=sys.stderr)

    sys_vals: dict[str, dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    sys_gens: dict[str, int] = defaultdict(int)

    t0 = time.time()
    n_tasks = 0
    n_gens = 0
    with ZJS_SRC.open("rb") as f:
        for task in ijson.items(f, "tasks.item"):
            n_tasks += 1
            for gen in task.get("generations") or []:
                mid = gen.get("model_id")
                if not mid:
                    continue
                n_gens += 1
                sys_gens[mid] += 1

                gen_metric_vals: dict[str, float] = {}
                gen_judge = {"judge_raw": None, "grade_points": None, "pass_rate": None}

                for ev in gen.get("evaluations") or []:
                    m = ev.get("metrics") or {}
                    if "llm_judge_falloesung" in m and gen_judge["judge_raw"] is None:
                        judge = m["llm_judge_falloesung"]
                        details = (judge.get("details")
                                   if isinstance(judge, dict) else None) or {}
                        raw = details.get("raw_score") if isinstance(details, dict) else None
                        if raw is None:
                            raw = m.get("raw_score")
                        if raw is None and isinstance(judge, (int, float)):
                            raw = float(judge) * 100.0
                        gp = (details.get("grade_points")
                              if isinstance(details, dict) else None)
                        if gp is None:
                            gp = m.get("llm_judge_falloesung_grade_points")
                        passed = (details.get("passed")
                                  if isinstance(details, dict) else None)
                        if passed is None:
                            passed = m.get("llm_judge_falloesung_passed")
                        gen_judge["judge_raw"] = _to_float(raw)
                        gen_judge["grade_points"] = _to_float(gp)
                        gen_judge["pass_rate"] = (
                            1.0 if passed else (0.0 if passed is not None else None)
                        )
                    for k in ("bleu", "rouge", "meteor", "bertscore",
                              "moverscore", "semantic_similarity", "coherence"):
                        if k in m and k not in gen_metric_vals:
                            v = m[k]
                            if isinstance(v, dict):
                                v = v.get("value", v.get("score"))
                            v = _to_float(v)
                            if v is not None:
                                gen_metric_vals[k] = v

                for k, v in gen_judge.items():
                    if v is not None:
                        sys_vals[mid][k].append(v)
                for k, v in gen_metric_vals.items():
                    sys_vals[mid][k].append(v)

            if n_tasks % 50 == 0:
                el = time.time() - t0
                print(f"  ... {n_tasks} tasks, {n_gens} gens in {el:.1f}s",
                      file=sys.stderr)

    sys_order = sorted(
        sys_vals.keys(),
        key=lambda s: -(statistics.fmean(sys_vals[s].get("judge_raw") or [0.0])),
    )
    rows = [(s, {"n_generations": sys_gens[s]}, dict(sys_vals[s])) for s in sys_order]
    out = write_wide_csv(OUT_ZJS, rows, FALL_METRICS, system_label="system")
    print(f"[zjs] wrote {OUT_ZJS.relative_to(HERE)} ({len(out)} rows; "
          f"{n_tasks} tasks, {n_gens} gens, {time.time()-t0:.0f}s)")
    return rows


