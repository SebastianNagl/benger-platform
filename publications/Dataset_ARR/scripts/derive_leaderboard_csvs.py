"""Build per-system leaderboard CSVs with bootstrap 95% CIs for sharing.

Outputs (data/processed/):
  - leaderboard_benchathon.csv      humans (traditional), humans (co-creation),
                                    and each LLM system; LLM-judge + automatic
                                    metrics with bootstrap 95% CIs.
  - leaderboard_zjs.csv             each LLM system; same metric set.
  - leaderboard_grundprinzipien.csv each LLM system; custom-judge rubric +
                                    accuracy + automatic metrics.
  - leaderboard_combined.csv        all three datasets stacked, union of
                                    metrics. Cells are empty where a metric is
                                    not computed on a dataset (e.g. accuracy
                                    on Falllösung, coherence on ZJS).

Wide format: one row per system, columns = system, n_generations, then one
column per metric containing `mean [ci_low, ci_high]` (95% bootstrap
percentile CI, B=2000, seed=42) computed on per-generation values, matching
the manuscript's "bootstrap 95% CI" convention (see manuscript.qmd RQ4 prose).
"""

