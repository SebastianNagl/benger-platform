# Combined: stack all three datasets into one wide CSV.
# ---------------------------------------------------------------------------

def build_combined(bench_rows, zjs_rows, gp_rows):
    """Stack the three per-dataset row lists into one wide CSV.

    Grundprinzipien's `judge_pass_rate` is aliased into the shared `pass_rate`
    column — both represent the LLM judge's pass rate, just under different
    rubrics (10-dim Falllösung vs 4-dim custom). Cells stay empty where a
    metric is not computed on a dataset.

    `n_human_grades` is only populated for benchathon rows (ZJS / GP have no
    human grading).
    """
    count_cols = ("n_generations", "n_human_grades")
    header = ["dataset", "group_or_system"] + list(count_cols) + [
        f"{m} [95% CI]" for m in COMBINED_METRICS
    ]

    out_rows = []

    def _emit(dataset, rows, alias_map=None):
        alias_map = alias_map or {}
        for label, counts, per_metric in rows:
            row = [dataset, label] + [counts.get(c, "") for c in count_cols]
            for m in COMBINED_METRICS:
                src_key = alias_map.get(m, m)
                values = per_metric.get(src_key, [])
                mean, lo, hi, _n = bootstrap_ci(values)
                row.append(_format_metric_cell(m, mean, lo, hi))
            out_rows.append(row)

    _emit("benchathon", bench_rows)
    _emit("zjs", zjs_rows)
    _emit("grundprinzipien", gp_rows, alias_map={"pass_rate": "judge_pass_rate"})

    OUT_COMBINED.parent.mkdir(parents=True, exist_ok=True)
    with OUT_COMBINED.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(header)
        for r in out_rows:
            w.writerow(r)
    print(f"[combined] wrote {OUT_COMBINED.relative_to(HERE)} ({len(out_rows)} rows)")


def main():
    target = sys.argv[1] if len(sys.argv) > 1 else "all"
    bench_rows = zjs_rows = gp_rows = None
    if target in ("all", "benchathon", "combined"):
        bench_rows = build_benchathon()
    if target in ("all", "grundprinzipien", "gp", "combined"):
        gp_rows = build_grundprinzipien()
    if target in ("all", "zjs", "combined"):
        zjs_rows = build_zjs()
    if target in ("all", "combined"):
        build_combined(bench_rows or [], zjs_rows or [], gp_rows or [])


if __name__ == "__main__":
    main()
