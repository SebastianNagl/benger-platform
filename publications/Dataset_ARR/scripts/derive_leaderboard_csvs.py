def build_benchathon():
    print("[benchathon] loading raw export ...", file=sys.stderr)
    with BENCHATHON_SRC.open(encoding="utf-8") as f:
        export = json.load(f)
    tasks = export.get("tasks") or export
    variants = {}
    if VARIANTS.exists():
        variants = json.loads(VARIANTS.read_text(encoding="utf-8"))

    # --- Human rows: from task-level evaluations (LLM judge & auto metrics
    #     scored against the Musterlösung reference) ---
    # bucket: variant -> {metric -> [values per annotation]}
    # plus n unique annotations per variant.
    human_vals = {"no_ai": defaultdict(list), "ai": defaultdict(list)}
    human_anns = {"no_ai": set(), "ai": set()}

    # Track per-annotation/per-metric value (first occurrence) so repeated
    # judge runs don't double-count.
    seen = {"no_ai": defaultdict(set), "ai": defaultdict(set)}

    # Filter to GPT-5.4-mini primary single-pass run only; other
    # llm_judge_falloesung prefixes are cross-validation / intra-judge runs
    # that would average over multiple judges if included.
    PRIMARY_JUDGE_PREFIX = "llm_judge_falloesung-mptmfvee"
    for t in tasks:
        for ev in t.get("evaluations") or []:
            fn = ev.get("field_name") or ""
            if "human:loesung" not in fn:
                continue
            ann_id = ev.get("annotation_id")
            v = variants.get(ann_id)
            if v not in ("no_ai", "ai"):
                continue
            m = ev.get("metrics") or {}
            human_anns[v].add(ann_id)

            # Judge: pull raw, grade points, pass. Restrict to primary judge.
            if "llm_judge_falloesung" in m and fn.startswith(PRIMARY_JUDGE_PREFIX):
                details = m.get("llm_judge_falloesung_details") or {}
                if isinstance(m.get("llm_judge_falloesung"), dict):
                    details = (m["llm_judge_falloesung"].get("details") or details)
                raw = (details.get("raw_score")
                       if isinstance(details, dict) else None)
                if raw is None:
                    raw = m.get("llm_judge_falloesung_raw")
                # The new primary-judge run puts grade_points and passed
                # under nested details too; fall back to flat keys for
                # legacy runs.
                gp = (details.get("grade_points") if isinstance(details, dict) else None)
                if gp is None:
                    gp = m.get("llm_judge_falloesung_grade_points")
                passed = (details.get("passed") if isinstance(details, dict) else None)
                if passed is None:
                    passed = m.get("llm_judge_falloesung_passed")
                if raw is not None and "judge_raw" not in seen[v][ann_id]:
                    human_vals[v]["judge_raw"].append(_to_float(raw))
                    seen[v][ann_id].add("judge_raw")
                if gp is not None and "grade_points" not in seen[v][ann_id]:
                    human_vals[v]["grade_points"].append(_to_float(gp))
                    seen[v][ann_id].add("grade_points")
                if passed is not None and "pass_rate" not in seen[v][ann_id]:
                    human_vals[v]["pass_rate"].append(1.0 if passed else 0.0)
                    seen[v][ann_id].add("pass_rate")

            for k in ("bleu", "rouge", "meteor", "coherence",
                      "bertscore", "moverscore", "semantic_similarity"):
                if k in m and isinstance(m[k], dict):
                    val = _to_float(m[k].get("value"))
                    if val is not None and k not in seen[v][ann_id]:
                        human_vals[v][k].append(val)
                        seen[v][ann_id].add(k)

    # --- LLM-system rows: pull from processed exports (already deduped) ---
    print("[benchathon] loading model_evaluations + automatic_metrics ...", file=sys.stderr)
    model_evals = json.loads(MODEL_EVALS.read_text(encoding="utf-8"))
    auto_metrics = json.loads(AUTO_METRICS.read_text(encoding="utf-8"))

    sys_vals: dict[str, dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    sys_gens: dict[str, set] = defaultdict(set)

    for r in model_evals:
        sysn = r["system"]
        gid = r.get("generation_id")
        if gid:
            sys_gens[sysn].add(gid)
        if r.get("raw_score") is not None:
            sys_vals[sysn]["judge_raw"].append(_to_float(r["raw_score"]))
        if r.get("grade_points") is not None:
            sys_vals[sysn]["grade_points"].append(_to_float(r["grade_points"]))
        if r.get("passed") is not None:
            sys_vals[sysn]["pass_rate"].append(1.0 if r["passed"] else 0.0)

    for r in auto_metrics:
        sysn = r["system"]
        k = r["metric"]
        v = _to_float(r.get("value"))
        if v is not None:
            sys_vals[sysn][k].append(v)

    # --- Human-grader (korrektur_falloesung) scores: merge in by row label.
    #     This file is mock today; once real expert grading lands it just gets
    #     regenerated by derive_paper_exports.py and we pick it up here.
    human_grade_buckets, human_grade_counts = _load_human_grade_buckets()
    n_hg_total = sum(human_grade_counts.values())
    print(f"[benchathon] human grades: {n_hg_total} rows across "
          f"{len(human_grade_counts)} buckets "
          f"({'real' if HUMAN_GRADES.exists() and n_hg_total else 'mock/empty'})",
          file=sys.stderr)

    def _with_human(label, per_metric):
        merged = dict(per_metric)
        for k, vs in (human_grade_buckets.get(label) or {}).items():
            merged[k] = vs
        return merged

    # Rank systems by judge_raw mean (descending).
    sys_order = sorted(
        sys_vals.keys(),
        key=lambda s: -(statistics.fmean(sys_vals[s].get("judge_raw") or [0.0])),
    )

    rows = []
    rows.append((
        "Human (traditional)",
        {
            "n_generations": len(human_anns["no_ai"]),
            "n_human_grades": human_grade_counts.get("Human (traditional)", 0),
        },
        _with_human("Human (traditional)", human_vals["no_ai"]),
    ))
    rows.append((
        "Human (co-creation)",
        {
            "n_generations": len(human_anns["ai"]),
            "n_human_grades": human_grade_counts.get("Human (co-creation)", 0),
        },
        _with_human("Human (co-creation)", human_vals["ai"]),
    ))
    for s in sys_order:
        rows.append((
            s,
            {
                "n_generations": len(sys_gens[s]),
                "n_human_grades": human_grade_counts.get(s, 0),
            },
            _with_human(s, sys_vals[s]),
        ))

    out = write_wide_csv(
        OUT_BENCH, rows, FALL_METRICS,
        system_label="group_or_system",
        count_columns=("n_generations", "n_human_grades"),
    )
    print(f"[benchathon] wrote {OUT_BENCH.relative_to(HERE)} ({len(out)} rows)")
    return rows


